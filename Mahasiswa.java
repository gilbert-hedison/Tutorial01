
public class Mahasiswa {
	private int npm;
	private String nama;
	public Mahasiswa(int npm, String nama) {
		super();
		this.npm = npm;
		this.nama = nama;
	}
	public int getNpm() {
		return npm;
	}
	public void setNpm(int npm) {
		this.npm = npm;
	}
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	
}
