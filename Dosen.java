
public class Dosen {
	private int noID;
	private String nama;
	public Dosen(int noID, String nama) {
		super();
		this.noID = noID;
		this.nama = nama;
	}
	public int getNoID() {
		return noID;
	}
	public void setNoID(int noID) {
		this.noID = noID;
	}
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	
}
